std::vector<uint8_t> load_font()
{
  std::vector<uint8_t> font;
  unsigned int len = strlen(font_str);
  uint8_t byte = 0;
  for (unsigned int i=0; i<len; i++)
  {
    int bit = i%8;
    if (font_str[i] == '#')
    {
      byte |= 1<<(7-bit);
    }
    else
    {
      byte &= ~(1<<(7-bit));
    }
    if (bit == 7)
    {
      font.push_back(byte);
    }
  }
  return font;
}
